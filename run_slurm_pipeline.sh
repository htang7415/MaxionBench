detect_cluster_env_name() {
  local -a args=("$@")
  local idx=0
  local cluster_name=""
  local slurm_profile_name=""
  while [[ ${idx} -lt ${#args[@]} ]]; do
    case "${args[${idx}]}" in
      --cluster)
        if [[ $((idx + 1)) -lt ${#args[@]} ]]; then
          cluster_name="${args[$((idx + 1))]}"
        fi
        idx=$((idx + 2))
        continue
        ;;
      --slurm-profile)
        if [[ $((idx + 1)) -lt ${#args[@]} ]]; then
          slurm_profile_name="${args[$((idx + 1))]}"
        fi
        idx=$((idx + 2))
        continue
        ;;
    esac
    idx=$((idx + 1))
  done
  if [[ -n "${cluster_name}" ]]; then
    printf '%s\n' "${cluster_name}"
    return 0
  fi
  case "${slurm_profile_name}" in
    euler_apptainer)
      printf '%s\n' "euler"
      ;;
    nrel_apptainer)
      printf '%s\n' "nrel"
      ;;
  esac
}

load_cluster_env_defaults() {
  local cluster_name="$1"
  if [[ -z "${cluster_name}" ]]; then
    return 0
  fi
  local env_file="${ROOT_DIR}/.env.slurm.${cluster_name}"
  if [[ ! -f "${env_file}" ]]; then
    return 0
  fi

  local -a preserve_vars=(
    MAXIONBENCH_SHARED_ROOT
    MAXIONBENCH_DATASET_ROOT
    MAXIONBENCH_DATASET_CACHE_DIR
    MAXIONBENCH_OUTPUT_ROOT
    MAXIONBENCH_FIGURES_ROOT
    MAXIONBENCH_HF_CACHE_DIR
    MAXIONBENCH_APPTAINER_CACHE_DIR
    MAXIONBENCH_APPTAINER_TMPDIR
    MAXIONBENCH_CONTAINER_IMAGE
    MAXIONBENCH_QDRANT_IMAGE
    MAXIONBENCH_PGVECTOR_IMAGE
    MAXIONBENCH_OPENSEARCH_IMAGE
    MAXIONBENCH_WEAVIATE_IMAGE
    MAXIONBENCH_MILVUS_ETCD_IMAGE
    MAXIONBENCH_MILVUS_MINIO_IMAGE
    MAXIONBENCH_MILVUS_IMAGE
    MAXIONBENCH_SLURM_ACCOUNT
    MAXIONBENCH_SLURM_PARTITION
    MAXIONBENCH_APPTAINER_MODULE
    MAXIONBENCH_MODULE_INIT_SH
    MAXIONBENCH_CLEANUP_LOCAL_SCRATCH
    MAXIONBENCH_CRAG_EXAMPLES
  )
  local -a preserve_set=()
  local -a preserve_values=()
  local idx=0
  local var_name=""
  for var_name in "${preserve_vars[@]}"; do
    if [[ "${!var_name+x}" == x ]]; then
      preserve_set[${idx}]=1
      preserve_values[${idx}]="${!var_name}"
    else
      preserve_set[${idx}]=0
      preserve_values[${idx}]=""
    fi
    idx=$((idx + 1))
  done

  # shellcheck disable=SC1090
  source "${env_file}"

  idx=0
  for var_name in "${preserve_vars[@]}"; do
    if [[ "${preserve_set[${idx}]}" == "1" ]]; then
      printf -v "${var_name}" '%s' "${preserve_values[${idx}]}"
      export "${var_name}"
    fi
    idx=$((idx + 1))
  done
  printf '%s\n' "+ loaded cluster env ${env_file}"
}

cluster_env_file_path() {
  local cluster_name="$1"
  if [[ -z "${cluster_name}" ]]; then
    return 1
  fi
  printf '%s\n' "${ROOT_DIR}/.env.slurm.${cluster_name}"
}

cluster_env_name() {
  if [[ -n "${CLUSTER}" ]]; then
    printf '%s\n' "${CLUSTER}"
    return 0
  fi
  if [[ -n "${AUTO_CLUSTER_ENV_NAME}" ]]; then
    printf '%s\n' "${AUTO_CLUSTER_ENV_NAME}"
    return 0
  fi
  case "${SLURM_PROFILE}" in
    euler_apptainer)
      printf '%s\n' "euler"
      ;;
    nrel_apptainer)
      printf '%s\n' "nrel"
      ;;
    *)
      return 1
      ;;
  esac
}

is_placeholder_value() {
  local value="$1"
  case "${value}" in
    your-account|YOUR_PRIVATE_ACCOUNT|your-partition|YOUR_PRIVATE_PARTITION)
      return 0
      ;;
    /shared/containers/*)
      return 0
      ;;
    *)
      return 1
      ;;
  esac
}

validate_cluster_defaults() {
  local value=""
  for value in \
    "${MAXIONBENCH_SLURM_ACCOUNT:-}" \
    "${MAXIONBENCH_SLURM_PARTITION:-}" \
    "${MAXIONBENCH_CONTAINER_IMAGE:-}" \
    "${MAXIONBENCH_QDRANT_IMAGE:-}" \
    "${MAXIONBENCH_PGVECTOR_IMAGE:-}" \
    "${MAXIONBENCH_OPENSEARCH_IMAGE:-}" \
    "${MAXIONBENCH_WEAVIATE_IMAGE:-}" \
    "${MAXIONBENCH_MILVUS_ETCD_IMAGE:-}" \
    "${MAXIONBENCH_MILVUS_MINIO_IMAGE:-}" \
    "${MAXIONBENCH_MILVUS_IMAGE:-}"
  do
    if [[ -z "${value}" ]]; then
      continue
    fi
    if is_placeholder_value "${value}"; then
      echo "error: placeholder cluster-local value detected: ${value}" >&2
      echo "error: remove copied example values or override them with real cluster settings" >&2
      exit 2
    fi
  done
}


resolve_root_path() {
  local raw_path="$1"
  if [[ "${raw_path}" = /* ]]; then
    printf '%s\n' "${raw_path}"
    return 0
  fi
  printf '%s\n' "${ROOT_DIR}/${raw_path}"
}

default_container_path() {
  local current_path="$1"
  local image_name="$2"
  if [[ -n "${current_path}" ]]; then
    printf '%s\n' "${current_path}"
    return 0
  fi
  printf '%s\n' "${DEFAULT_SHARED_ROOT}/containers/${image_name}"
}

default_apptainer_cache_dir() {
